package org.tikware.user;

public class UserInfo {
    private String id;
    private String user;
    private String password;
    private Character privilege;
    private String joinTime;

    public static final Character ADMIN = 'L';
    public static final Character MANAGER = 'M';
    public static final Character USER = 'N';

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public Character getPrivilege() {
        return privilege;
    }

    public void setPrivilege(Character privilege) {
        this.privilege = privilege;
    }

    public String getJoinTime() {
        return joinTime;
    }

    public void setJoinTime(String joinTime) {
        this.joinTime = joinTime;
    }
}
